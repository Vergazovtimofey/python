
def num_translate(number):
    if number == 'один':
        print('один')
    elif number == 'two':
        print('два')

    elif number == 'three':
        print('три')

    elif number == 'four':
        print('четыре')

    elif number == 'five':
        print('пять')

    elif number == 'six':
        print('шесть')

    elif number == 'seven':
        print('семь')

    elif number == 'eighth':
        print('весемь')

    elif number == 'nine':
        print('девять')

    elif number == 'ten':
        print('десять')
    else:
        print( "None")


num_translate("ten")





