def num_translate_adv(number):

    if number == 'один':
        print('один')
    elif number == 'One':
        print('Один')

    elif number == 'two':
        print('два')
    elif number == 'Two':
        print('Два')

    elif number == 'three':
        print('три')
    elif number == 'Three':
        print('Три')

    elif number == 'four':
        print('четыре')
    elif number == 'Four':
        print('Четыре')

    elif number == 'five':
        print('пять')
    elif number == 'Five':
        print('Пять')

    elif number == 'six':
        print('шесть')
    elif number == 'Six':
        print('Шесть')

    elif number == 'seven':
        print('семь')
    elif number == 'Seven':
        print('Семь')

    elif number == 'eighth':
        print('весемь')
    elif number == 'Eighth':
        print('Восемь')

    elif number == 'nine':
        print('девять')
    elif number == 'Nine':
        print('Девять')

    elif number == 'ten':
        print('десять')
    elif number == 'Ten':
        print('Десять')

    else:
        print("None")


num_translate_adv('Two')